package set단위class;

// 목표 무게x횟수 set
public class wc_set extends set{
	private int weight;
	private int count;
	
	public wc_set() {
		weight = 0;
		count = 0;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
}
